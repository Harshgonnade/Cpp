# include<iostream>
using namespace std;

int main()
{
    // for loop
    for (int i = 0; i < 5; i++)
    {
        cout<<i<<endl;
    }
    cout<<endl;
    // while loop
    int j=0;
    while (j<5)
    {
        cout<<j<<endl;
        j++;
    }
    cout<<endl;
    // do while loop
    int k=0;
    do
    {
        cout<<k<<endl;
        k++;
    } while (k<5);

    return 0;
}